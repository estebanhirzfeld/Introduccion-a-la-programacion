public class task_1 {
    public static void main(String[] args) {
        Integer nro = 1;
        String texto = "Hello world";
        Boolean result = false;

        // Suma
        System.out.println(nro + 1);

        // Concatenacion
        System.out.println(texto + " " + nro);

        // Comparacion
        System.out.println(nro == 1);

        // Conversion
        System.out.println((double)nro);

        // Mutacion
        System.out.println(result);
        result = true;
        System.out.println(result);
    }
}